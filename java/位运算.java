public class 位运算 {
    public static void main(String[] args) {
        /*
         * int a = 1>>2
         * 1 00000000 00000000 00000000 00000001
         * 整体右移，溢出位丢弃 用零填补空缺
         * 00000000 00000000 00000000 00000000 结果 0
         */
          /*
         * int a = 1<<2
         * 1 00000000 00000000 00000000 00000001
         * 整体左移，溢出位丢弃 用零填补空缺
         * 00000000 00000000 00000000 00000100 结果 4
         */
        System.out.println(1<<2);
        System.out.println(1>>2);
    }

}
