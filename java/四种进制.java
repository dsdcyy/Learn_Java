public class 四种进制{
    public static  void main(String[] args) {
        // 二进制 0b开头，范围0-1
        int a = 0b1100;
        System.out.println(a);
        // 八进制 0开头范围0-7
        int d = 01100;
        System.out.println(d);
        // 十进制 范围 0-9
        int b = 1100;
        System.out.println(b);

        // 十六进制 0x开头 范围0-9a-f
        int c = 0x1100;
        System.out.println(c);

    }
}